// BOOLEAN - retorna verdadeiro(true) ou falso (false)
// COMPARACAO NUMBER
const numero1 = 5
const numero2 = 10 
console.log(numero1 === numero2)// false

//COMPARACAO STRING
const texto1 = "salve"
const texto2 = "SALVE" 
console.log(texto1 === texto2);// false

//  COMPARACAO USANDO IF / ELSE
const nums1 = 50
const nums2 = 100

if(nums1 === nums2){
    console.log("verdade")
} else{
    console.log("falso")
}// falso